public class Empleado {
    
    String nombre;
    float importe;

    public Empleado(String nombre) {
        this.nombre = nombre;
     
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public float getImporte() {
        return importe;
    }

    public void setImporte(float importe) {
        this.importe = importe;
    }
    
    
    
    
    
}
